// Generate 30 days of historical data
export function generateHistoricalData(vaultId: string): HistoricalDataPoint[] {
  const vaultConfig: Record<string, { baseAPY: number; baseTVL: number; baseSharePrice: number }> = {
    'elp-stable-vault': { baseAPY: 18, baseTVL: 4_800_000, baseSharePrice: 1.07 },
    'elp-sol-vault': { baseAPY: 34, baseTVL: 3_100_000, baseSharePrice: 1.21 },
    'elp-defi-vault': { baseAPY: 67, baseTVL: 1_850_000, baseSharePrice: 1.84 },
    'elp-btc-vault': { baseAPY: 12, baseTVL: 6_000_000, baseSharePrice: 1.03 },
    'elp-meme-vault': { baseAPY: 142, baseTVL: 900_000, baseSharePrice: 2.1 },
    'elp-rwa-vault': { baseAPY: 7.8, baseTVL: 8_200_000, baseSharePrice: 1.01 },
  }

  const config = vaultConfig[vaultId] || { baseAPY: 18, baseTVL: 4_800_000, baseSharePrice: 1.07 }
  const points: HistoricalDataPoint[] = []
  const now = Date.now()
  
  for (let i = 30; i >= 0; i--) {
    const t = now - i * 24 * 3600000
    const noise = () => (Math.random() - 0.48) * 0.04
    const trend = (30 - i) / 30
    
    points.push({
      timestamp: t,
      tvl: config.baseTVL * (1 - trend * 0.1 + noise() * 3),
      apy: config.baseAPY * (1 + noise() * 0.5),
      sharePrice: config.baseSharePrice * (1 - (30 - i) * 0.002 + noise() * 0.3),
      volume: Math.random() * 800_000 + 200_000,
    })
  }
  return points
}

